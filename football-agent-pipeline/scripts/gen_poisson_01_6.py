bel_def_index = bel_def_actual / baseline_def
esp_dri = esp_scc * bel_def_index
esp_def_index = esp_def_floor / baseline_def
bel_dri = bel_scc * esp_def_index
esp_dri_pct = (esp_dri / esp_scc - 1) * 100
bel_dri_pct = (bel_dri / bel_scc - 1) * 100

# ============================================================
# STEP 3: GIF
# ============================================================
