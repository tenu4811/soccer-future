esp_tpm_pct = (esp_final / esp_gif - 1) * 100
bel_tpm_pct = (bel_final / bel_gif - 1) * 100

# ============================================================
# POISSON PROBABILITIES
# ============================================================
def poisson(lam, k):
    return math.exp(-lam) * (lam**k) / math.factorial(k)

esp_p = [poisson(esp_final, k) for k in range(9)]
bel_p = [poisson(bel_final, k) for k in range(6)]
bel_4plus = 1 - sum(bel_p[:4])

# Score matrix
matrix = {}
for h in range(9):
    for a in range(5):
        pa = bel_p[a] if a <= 3 else bel_4plus
        matrix[(h, a)] = esp_p[h] * pa

