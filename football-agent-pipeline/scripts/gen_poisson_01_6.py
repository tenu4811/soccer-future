esp_gif_pct = (esp_gif / esp_dri - 1) * 100
bel_gif_pct = (bel_gif / bel_dri - 1) * 100

# ============================================================
# STEP 4: TPM
# ============================================================
esp_tpm_total = 0.11   # +11%
